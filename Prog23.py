# All no From 1 to 100 using While Loop

a = 1
while a <= 100:
    print(a)
    a += 1

# All no From 1 to 100 in a Single Line using While Loop

a = 1
while a <= 100:
    print(a, end=" ")
    a += 1

# All no From 1 to 100 by using Separator Comma (,) using While Loop

a = 1
while a <= 100:
    print(a, end=",")
    a += 1
