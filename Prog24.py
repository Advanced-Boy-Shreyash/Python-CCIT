# All Odd no from 1 to 100

a = 1
while a <= 100:
    if a % 2 != 0:
        print(a)
    a += 1

# All Even no from 1 to 100

a = 1
while a <= 100:
    if a % 2 == 0:
        print(a)
    a += 1

# All Odd no from 100 to 1

a = 100
while a >= 1:
    if a % 2 != 0:
        print(a)
    a -= 1

